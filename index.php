      let currentIndex = 0;

      function showImage(index) {
          const slider = document.querySelector('.slider');
          const imageWidth = document.querySelector('.slider img').clientWidth;
          slider.style.transform = `translateX(${-index * imageWidth}px)`;
          currentIndex = index;
      }

      function autoSlide() {
          currentIndex = (currentIndex + 1) % document.querySelectorAll('.slider img').length;
          showImage(currentIndex);
      }

      // Auto slide to the next image every 5 seconds (adjust as needed)
      setInterval(autoSlide, 5000);

      // Initial setup
      showImage(currentIndex);
